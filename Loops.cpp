#include<bits/stdc++.h>
using namespace std;
int main(){
    //For loop
    int i=1;
    for(i;i<=10;i++){
        cout<<"Hi I am "<<i<<endl;
    }
    //While Loop
    while(i<=5){
       cout<<"Hi I am while"<< i<<endl;
       i=i+1;
    }
    //Do While
    do{
        cout<<"Hi I am do while"<< i<<endl;
        i++;
    }while(i<=1);
}
